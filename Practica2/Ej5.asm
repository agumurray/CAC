ORG 1000H
MSJ DB "INGRESE UN NUMERO:"
FIN1 DB ?
ERROR DB "CARACTER NO VALIDO";
FIN2 DB ?
NUM DB ?




ORG 3000H
ES_NUM: CMP BYTE PTR [BX], 30H 
JS NO_NUM
CMP BYTE PTR [BX], 39H
JZ SI_NUM
JNS NO_NUM
JMP SI_NUM
NO_NUM: MOV CL, 00H
JMP FINNN
SI_NUM: MOV CL, 0FFH
FINNN: RET



ORG 2000H
MOV BX, OFFSET MSJ
MOV AL, OFFSET FIN1-OFFSET MSJ
INT 7
MOV BX, OFFSET NUM
INT 6
CALL ES_NUM
CMP CL, 0FFH
JZ PRINT_NUMERO
MOV BX, OFFSET ERROR
MOV AL, OFFSET FIN2 - OFFSET ERROR
INT 7
JMP FINN
PRINT_NUMERO: MOV AL, 1
INT 7
MOV CX, 0
MOV CL, NUM
FINN: INT 0
END